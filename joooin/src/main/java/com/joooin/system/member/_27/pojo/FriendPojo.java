package com.joooin.system.member._27.pojo;

public class FriendPojo {
	private Integer memberId;
	private String memberName;
	private Boolean isFriend;
	private Boolean isInviter;
	private String messageHash;
	private Integer notRead;
	private String messageText;
	private Integer memberChatId;
	
	public Integer getMemberId() {
		return memberId;
	}
	public void setMemberId(Integer memberId) {
		this.memberId = memberId;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public Boolean getIsFriend() {
		return isFriend;
	}
	public void setIsFriend(Boolean isFriend) {
		this.isFriend = isFriend;
	}
	public Boolean getIsInviter() {
		return isInviter;
	}
	public void setIsInviter(Boolean isInviter) {
		this.isInviter = isInviter;
	}
	public String getMessageHash() {
		return messageHash;
	}
	public void setMessageHash(String messageHash) {
		this.messageHash = messageHash;
	}
	public Integer getNotRead() {
		return notRead;
	}
	public void setNotRead(Integer notRead) {
		this.notRead = notRead;
	}
	public String getMessageText() {
		return messageText;
	}
	public void setMessageText(String messageText) {
		this.messageText = messageText;
	}
	public Integer getMemberChatId() {
		return memberChatId;
	}
	public void setMemberChatId(Integer memberChatId) {
		this.memberChatId = memberChatId;
	}
	
	
}
